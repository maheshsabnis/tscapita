// Union Type
// Defining a variable with More-than-One datatype

// Even if the number or string data  is not assigned to 'value' we can still successfully use value with 'null'
let value:number|string|undefined;

value = 100;
console.log('value = ' + value + '  and type of value = ' + typeof(value));
value = 'James';
console.log('value = ' + value + '  and type of value = ' + typeof(value));


 

